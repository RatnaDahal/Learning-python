#WAP to calculates Simple interest basead on user input for principle,rate and time
P=float(input("Enter the principle to calculate simple interest: "))
R=float(input("Enter the rate to calculate simple interet: "))
t=float(input("Enter the time to calculate simple interet: "))

SI= (P*t*R)/100

print(f"Total amount of simple interest is {SI}")

 

